// solicite ao usuario que informe a base e a altura de um retangulo

// calcule e exiba a area do retangulo

// formula area = base * altura

const base = parseFloat(prompt("Informe a base do retângulo:"));
const altura = parseFloat(prompt("Informe a altura do retângulo:"));

const area = base * altura;

alert(`A área do retângulo é: ${area}`);
